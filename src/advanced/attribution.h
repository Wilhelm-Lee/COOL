/*
 * ============================= INTRODUCTION ==================================
 * Attributions are labels on objects.
 * Specific functions & objects could access the attributions signed on any
 * object within its given permissions.
 *
 * In the current stage, possible prefixed attributions could be:
 *    Nullable
 *    NotNull
 *
 * Attributions can be NULL. But nulled attributions
 * cannot be accessed (ineffective).
 *
 * Attributions are customisable.  You can create new attributions and define
 * their behaviours.
 *
 * Attributions are inheritable.  You can inherit any attribution for your
 * attributions.
 *
 * Attributions cannot have attributions.  Causing recursive definitions is not
 * pleasant.
 * ============================= INTRODUCTION ==================================
*/

